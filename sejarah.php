            <h3>Sejarah Singkat</h3>
            <p>
					Bidang Infrastruktur dan Kewilayahan (Infraswil) merupakan salah satu unit kerja yang 
                ada di Bappelitbangsu, yang berdiri dengan tujuan untuk mengelola dan merencanakan 
                pembangunan infrastruktur yang strategis di wilayah Sumatera Utara. Sejak berdirinya, 
                Bidang Infraswil telah memainkan peran penting dalam merumuskan kebijakan teknis yang 
                meliputi berbagai sektor infrastruktur, sumber daya air, dan pembangunan wilayah.
				Seiring berjalannya waktu, Bidang Infraswil terus berinovasi dengan mengembangkan berbagai 
                proyek besar yang mendukung kesejahteraan masyarakat. Selain itu, dengan adanya dukungan 
                dari berbagai pihak, Bidang Infraswil telah berhasil menciptakan banyak kebijakan yang 
                mencakup pembangunan infrastruktur transportasi, penyediaan air bersih, serta pengembangan 
                kawasan kewilayahan yang lebih baik.
            </p>
            <p>
					Salah satu pencapaian terbesar Bidang Infraswil adalah keterlibatannya dalam Program 
                Pembangunan Infrastruktur Nasional, di mana proyek-proyek strategis yang dilaksanakan 
                memberikan dampak signifikan terhadap kemajuan pembangunan di berbagai daerah di Sumatera Utara.
                Proyek-proyek ini tidak hanya mengutamakan kemajuan fisik, tetapi juga menjaga keberlanjutan 
                lingkungan hidup.
				Dalam mencapai tujuannya, Bidang Infraswil senantiasa bekerja sama dengan berbagai pihak, 
                baik dari sektor pemerintahan, swasta, maupun masyarakat. Komitmen terhadap pembangunan 
                berkelanjutan menjadi bagian penting dari visi dan misi yang selalu dijaga oleh Bidang Infraswil.
            Hingga saat ini, Bidang Infraswil terus berkembang dan beradaptasi dengan tantangan zaman, 
                mengedepankan teknologi dan inovasi dalam merencanakan dan melaksanakan pembangunan 
                infrastruktur yang mendukung kualitas hidup masyarakat Sumatera Utara yang lebih baik.
            </p>
